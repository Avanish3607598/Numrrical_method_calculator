    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
     try{
        if (evt.getSource() == jButton4) {
            double a, b, c, d, m, n;

            a = Double.parseDouble(jTextField1.getText());
            b = Double.parseDouble(jTextField2.getText());
            c = Double.parseDouble(jTextField3.getText());
            d = Double.parseDouble(jTextField4.getText());
            m = Double.parseDouble(jTextField6.getText());
            n = Double.parseDouble(jTextField5.getText());
            int t = Integer.parseInt(jTextField7.getText());

//            System.out.println("Sequence of approximate Solution\n\n");
            double fx, fx1, fx2, fx3, fx_3;
            double x0 = (m + n) / 2;
            fx1 = a * m * m * m + b * m * m + c * m + d;
            fx2 = a * n * n * n + b * n * n + c * n + d;
            fx = fx1 * fx2;
            if (fx < 0) {
                for (int i = 1; i <= t; i++) {

                    fx3 = a * x0 * x0 * x0 + b * x0 * x0 + c * x0 + d;
                    fx_3 = a * 3 * x0 * x0 + b * 2 * x0 + c;
                    double x1 = x0 - (fx3 / fx_3);
                    x0 = x1;

//                    System.out.println(x0);
                    jTextField8.setText(Double.toString(x1));
                }

            } else {
                jTextField8.setText("Wrong interval");
            }

        }
     }
     catch (NumberFormatException ae) {
            JOptionPane.showMessageDialog(null,"Either input fields are empty \n or wrong input");
        }
    }//GEN-LAST:event_jButton4MouseClicked

